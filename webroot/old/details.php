As a visitor to the Wyoming Firearms Experience (WFE) you will be able to see and explore the history of the firearm.
Then you can take the opportunity to expand that interest into a whole new dimension with a reproduction historic firearm.
Choose from a selection of "experiences" that will capture a point in time through the firearm of the day.
Each experience contains a piece of American history: the Mountain Men, the Cowboys, the Buffalo Hunter, the Gatling Gun and more.   
</p>

<p>
First, you will work with a trained instructor,
who not only puts the firearms in a historical perspective, but also guides you through the safety procedures and operation of your chosen firearms.
Next, you will move to a state-of-the-art firing range where, under the close supervision of that instructor,
you will actually be able to step back in time and fire a piece of history.
We are committed to providing a unique and unparalleled venue to safely promote the history of American firearms here at the Wyoming Firearms Experience!   
</p>
